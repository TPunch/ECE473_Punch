void adxl_write (uint8_t *pBuffer, uint8_t address, uint8_t size)
{
	uint8_t WriteAddr[2];

	WriteAddr[1] = 0x0A;		// Write instruction
	WriteAddr[0] = address;		// Address

	HAL_Delay(10);		// Short delay
	HAL_GPIO_WritePin(SP1_SS_GPIO_Port, SP1_SS_Pin, GPIO_PIN_RESET);	// Pull CS pin low to enable the slave
	HAL_SPI_Transmit(&hspi1, WriteAddr, 2, 100); 		// Transmit the write instruction and address
	HAL_SPI_Transmit(&hspi1, pBuffer, size, 100);		// Transmit the data to write
	HAL_Delay(10);		// Short delay
	HAL_GPIO_WritePin(SP1_SS_GPIO_Port, SP1_SS_Pin, GPIO_PIN_SET);		// Pull CS pin high to disable the slave
}

void adxl_read (uint8_t *pBuffer, uint8_t address, uint8_t size)
{
	uint8_t ReadAddr[2];

	ReadAddr[1] = 0x0B;		// Read instruction
	ReadAddr[0] = address;	// Address
	//ReadAddr[0] = 0x00;		// Blank

	HAL_Delay(10);		// Short delay
	HAL_GPIO_WritePin(GPIOB, SP1_SS_Pin, GPIO_PIN_RESET);	// Pull CS pin low to enable the slave
	HAL_SPI_Transmit(&hspi1, ReadAddr, 2, 100); 			// Transmit the data and address
	HAL_SPI_Receive(&hspi1, pBuffer, size, 100);			// Receive the data
	HAL_Delay(10);		// Short delay
	HAL_GPIO_WritePin(GPIOB, SP1_SS_Pin, GPIO_PIN_SET);		// Pull CS pin high to disable the slave

}

